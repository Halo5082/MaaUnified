    private static readonly Dictionary<string, string> Pallas = new(EnUs, StringComparer.OrdinalIgnoreCase);

    private string _language = UiLanguageCatalog.DefaultLanguage;

    public string Language
    {
        get => _language;
        set
        {
            var normalized = UiLanguageCatalog.Normalize(value);
            if (!SetProperty(ref _language, normalized))
            {
                return;
            }

            OnPropertyChanged("Item[]");
        }
    }

    public string this[string key]
    {
        get
        {
            var normalized = UiLanguageCatalog.Normalize(Language);
            var source = ResolveSource(normalized);
            if (source.TryGetValue(key, out var value))
            {
                return value;
            }

            if (EnUs.TryGetValue(key, out value))
            {
                return value;
            }

            return key;
        }
    }

    public string GetOrDefault(string key, string fallback)
    {
        if (string.IsNullOrWhiteSpace(key))
        {
            return fallback;
        }

        var value = this[key];
        return string.Equals(value, key, StringComparison.Ordinal) ? fallback : value;
    }

    private static IReadOnlyDictionary<string, string> ResolveSource(string normalizedLanguage)
    {
        return normalizedLanguage switch
        {
            "zh-cn" => ZhCn,
            "zh-tw" => ZhTw,
            "ja-jp" => JaJp,
            "ko-kr" => KoKr,
            "pallas" => Pallas,
            _ => EnUs,
        };
    }
